const day = 6; // domingo

switch (day) {
  case 0:
    console.log("Domingo");
    break;
  case 1:
    console.log("Lunes");
    break;
  case 2:
    console.log("Martes");
    break;
  case 3:
    console.log("Miércoles");
    break;

  default:
    console.log("No es de domingo a miércoles");
    break;
}
